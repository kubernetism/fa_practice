/**
 * Post stock adjustment to GL: Debit Variance Expense, Credit Inventory (or reverse)
 */
export async function postStockAdjustmentToGL(params: {
  tenantId: number
  adjustmentId: number
  branchId: number
  userId: number
  amount: number
  isAddition: boolean
}) {
  const lines: GLLine[] = []

  const inventoryAccount = await findAccount(params.tenantId, 'inventory')
  const varianceAccount =
    (await findAccount(params.tenantId, 'other_expense')) ||
    (await findAccountByType(params.tenantId, 'expense'))

  if (params.isAddition) {
    // Adding stock: Debit Inventory, Credit Variance
    if (inventoryAccount) {
      lines.push({
        accountId: inventoryAccount.id,
        debitAmount: String(params.amount),
        creditAmount: '0',
        description: 'Inventory adjustment (add)',
      })
    }
    if (varianceAccount) {
      lines.push({
        accountId: varianceAccount.id,
        debitAmount: '0',
        creditAmount: String(params.amount),
        description: 'Stock variance adjustment',
      })
    }
  } else {
    // Removing stock: Debit Variance, Credit Inventory
    if (varianceAccount) {
      lines.push({
        accountId: varianceAccount.id,
        debitAmount: String(params.amount),
        creditAmount: '0',
        description: 'Stock variance (damage/theft/correction)',
      })
    }
    if (inventoryAccount) {
      lines.push({
        accountId: inventoryAccount.id,
        debitAmount: '0',
        creditAmount: String(params.amount),
        description: 'Inventory reduction from adjustment',
      })
    }
  }

  if (lines.length === 0) return null

  return createAndPostEntry({
    tenantId: params.tenantId,
    description: `Stock Adjustment #${params.adjustmentId}`,
    referenceType: 'adjustment',
    referenceId: params.adjustmentId,
    branchId: params.branchId,
    userId: params.userId,
    lines,
  })
}

