'use server'

import { db } from '@/lib/db'
import {
  journalEntries,
  journalEntryLines,
  chartOfAccounts,
} from '@/lib/db/schema'
import { eq, and, sql, count } from 'drizzle-orm'

/**
 * Generate the next journal entry number for a tenant: JE-YYYY-NNNN
 */
async function nextEntryNumber(tenantId: number): Promise<string> {
  const year = new Date().getFullYear()
  const [result] = await db
    .select({ c: count() })
    .from(journalEntries)
    .where(
      and(
        eq(journalEntries.tenantId, tenantId),
        sql`EXTRACT(YEAR FROM ${journalEntries.entryDate}) = ${year}`
      )
    )
  const seq = String((result.c || 0) + 1).padStart(4, '0')
  return `JE-${year}-${seq}`
}

/**
 * Find a GL account by sub-type for a tenant. Returns null if not found.
 */
async function findAccount(tenantId: number, subType: string) {
  const [account] = await db
    .select()
    .from(chartOfAccounts)
    .where(
      and(
        eq(chartOfAccounts.tenantId, tenantId),
        eq(chartOfAccounts.accountSubType, subType),
        eq(chartOfAccounts.isActive, true)
      )
    )
    .limit(1)
  return account
}

async function findAccountByType(tenantId: number, accountType: string) {
  const [account] = await db
    .select()
    .from(chartOfAccounts)
    .where(
      and(
        eq(chartOfAccounts.tenantId, tenantId),
        eq(chartOfAccounts.accountType, accountType as any),
        eq(chartOfAccounts.isActive, true)
      )
    )
    .limit(1)
  return account
}

interface GLLine {
  accountId: number
  debitAmount: string
  creditAmount: string
  description?: string
}

/**
 * Create and auto-post a journal entry with the given lines.
 */
async function createAndPostEntry(params: {
  tenantId: number
  description: string
  referenceType: string
  referenceId: number
  branchId: number
  userId: number
  lines: GLLine[]
}) {
  const entryNumber = await nextEntryNumber(params.tenantId)

  const [entry] = await db
    .insert(journalEntries)
    .values({
      tenantId: params.tenantId,
      entryNumber,
      entryDate: new Date(),
      description: params.description,
      referenceType: params.referenceType,
      referenceId: params.referenceId,
      branchId: params.branchId,
      status: 'posted',
      isAutoGenerated: true,
      createdBy: params.userId,
      postedBy: params.userId,
      postedAt: new Date(),
    })
    .returning()

  if (params.lines.length > 0) {
    await db.insert(journalEntryLines).values(
      params.lines.map((line) => ({
        journalEntryId: entry.id,
        accountId: line.accountId,
        debitAmount: line.debitAmount,
        creditAmount: line.creditAmount,
        description: line.description || null,
      }))
    )
  }

  // Update account balances
  for (const line of params.lines) {
    const debit = Number(line.debitAmount) || 0
    const credit = Number(line.creditAmount) || 0
    const net = debit - credit

    await db
      .update(chartOfAccounts)
      .set({
        currentBalance: sql`${chartOfAccounts.currentBalance}::numeric + ${net}`,
        updatedAt: new Date(),
      })
      .where(eq(chartOfAccounts.id, line.accountId))
  }

  return entry
}

/**
 * Post a sale to GL: Debit Cash/AR, Credit Revenue + Tax Payable
 */
export async function postSaleToGL(params: {
  tenantId: number
  saleId: number
  branchId: number
  userId: number
  totalAmount: number
  taxAmount: number
  costOfGoods: number
  paymentMethod: string
}) {
  const lines: GLLine[] = []

  // Debit: Cash or AR
  const isCredit = ['credit', 'receivable'].includes(params.paymentMethod)
  const debitAccount = isCredit
    ? await findAccount(params.tenantId, 'accounts_receivable')
    : await findAccount(params.tenantId, 'cash')

  if (debitAccount) {
    lines.push({
      accountId: debitAccount.id,
      debitAmount: String(params.totalAmount),
      creditAmount: '0',
      description: isCredit ? 'Accounts receivable from sale' : 'Cash received from sale',
    })
  }

  // Credit: Revenue
  const revenueAccount =
    (await findAccount(params.tenantId, 'sales_revenue')) ||
    (await findAccountByType(params.tenantId, 'revenue'))
  if (revenueAccount) {
    const revenueAmount = params.totalAmount - params.taxAmount
    lines.push({
      accountId: revenueAccount.id,
      debitAmount: '0',
      creditAmount: String(revenueAmount),
      description: 'Sales revenue',
    })
  }

  // Credit: Tax payable (if tax > 0)
  if (params.taxAmount > 0) {
    const taxAccount = await findAccount(params.tenantId, 'accrued_expense')
    if (taxAccount) {
      lines.push({
        accountId: taxAccount.id,
        debitAmount: '0',
        creditAmount: String(params.taxAmount),
        description: 'Tax collected on sale',
      })
    }
  }

  // COGS entry: Debit COGS, Credit Inventory
  if (params.costOfGoods > 0) {
    const cogsAccount = await findAccount(params.tenantId, 'cost_of_goods_sold')
    const inventoryAccount = await findAccount(params.tenantId, 'inventory')

    if (cogsAccount) {
      lines.push({
        accountId: cogsAccount.id,
        debitAmount: String(params.costOfGoods),
        creditAmount: '0',
        description: 'Cost of goods sold',
      })
    }
    if (inventoryAccount) {
      lines.push({
        accountId: inventoryAccount.id,
        debitAmount: '0',
        creditAmount: String(params.costOfGoods),
        description: 'Inventory reduction for sale',
      })
    }
  }

  if (lines.length === 0) return null

  return createAndPostEntry({
    tenantId: params.tenantId,
    description: `Sale #${params.saleId}`,
    referenceType: 'sale',
    referenceId: params.saleId,
    branchId: params.branchId,
    userId: params.userId,
    lines,
  })
}

/**
 * Reverse a sale GL entry (void/return)
 */
export async function postReturnToGL(params: {
  tenantId: number
  returnId: number
  originalSaleId: number
  branchId: number
  userId: number
  refundAmount: number
  taxAmount: number
  costOfGoods: number
  refundMethod: string
}) {
  const lines: GLLine[] = []

  // Credit Cash/AR (refund out)
  const isCreditRefund = params.refundMethod === 'store_credit'
  const cashAccount = isCreditRefund
    ? await findAccount(params.tenantId, 'accounts_receivable')
    : await findAccount(params.tenantId, 'cash')

  if (cashAccount) {
    lines.push({
      accountId: cashAccount.id,
      debitAmount: '0',
      creditAmount: String(params.refundAmount),
      description: 'Refund issued',
    })
  }

  // Debit Revenue (reverse)
  const revenueAccount =
    (await findAccount(params.tenantId, 'sales_revenue')) ||
    (await findAccountByType(params.tenantId, 'revenue'))
  if (revenueAccount) {
    lines.push({
      accountId: revenueAccount.id,
      debitAmount: String(params.refundAmount - params.taxAmount),
      creditAmount: '0',
      description: 'Revenue reversal for return',
    })
  }

  // Debit Tax payable (reverse)
  if (params.taxAmount > 0) {
    const taxAccount = await findAccount(params.tenantId, 'accrued_expense')
    if (taxAccount) {
      lines.push({
        accountId: taxAccount.id,
        debitAmount: String(params.taxAmount),
        creditAmount: '0',
        description: 'Tax reversal for return',
      })
    }
  }

  // Reverse COGS: Debit Inventory, Credit COGS
  if (params.costOfGoods > 0) {
    const cogsAccount = await findAccount(params.tenantId, 'cost_of_goods_sold')
    const inventoryAccount = await findAccount(params.tenantId, 'inventory')

    if (inventoryAccount) {
      lines.push({
        accountId: inventoryAccount.id,
        debitAmount: String(params.costOfGoods),
        creditAmount: '0',
        description: 'Inventory restored from return',
      })
    }
    if (cogsAccount) {
      lines.push({
        accountId: cogsAccount.id,
        debitAmount: '0',
        creditAmount: String(params.costOfGoods),
        description: 'COGS reversal for return',
      })
    }
  }

  if (lines.length === 0) return null

  return createAndPostEntry({
    tenantId: params.tenantId,
    description: `Return #${params.returnId} (Sale #${params.originalSaleId})`,
    referenceType: 'return',
    referenceId: params.returnId,
    branchId: params.branchId,
    userId: params.userId,
    lines,
  })
}

